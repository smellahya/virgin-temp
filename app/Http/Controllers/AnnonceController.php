     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'titre' => 'required'
        ]);

        $annonce = new Annonce();
        $annonce->titre = $request->input('titre', 'walo');
        $annonce->name = $request->input('name', 'walo2');
        $annonce->save();
        return redirect('/annonces')->with('success','created');
    }

